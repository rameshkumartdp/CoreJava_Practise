package spring.factorybean;

import java.util.Calendar;

public class Demo {
	private String name;
	private Calendar cal;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Calendar getCal() {
		return cal;
	}

	public void setCal(Calendar cal) {
		this.cal = cal;
	}
}